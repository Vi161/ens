            <p class="content-p">
                The device accumulates information about the body — dynamics of reaction within a certain period of
                time of action. Next analysis is based on the next period of time. This principle is used in simple
                COSMODIC devices.
            </p>
            <p class="content-p">
                In more complicated models this period is “sliding” along the time of action. In other words, we discard
                the oldest measurement at the “tail” and add a new measurement at the “head” and the dynamics of reaction
                is being completely recalculated.
            </p>
            <p class="content-p">
                From the specified period of time, in addition to the speed of reaction, we also select 46 spectral
                bands with the FFT method. There are 46 of them in most complicated devices or in a perfect device.
                In simple COSMODICs we use one central band and two side bands, i.e. three in total. In our "ancient"
                devices, like SCENAR 035, there was only one, central band, which was singled out by RC differentiating
                network. Dynamics of change of amplitudes of the spectrum bands within the time is followed up too,
                i.e. the first derivative of each spectrum component is being calculated.
            </p>
            <p class="content-p">
                In “active” COSMODIC system, according to dynamics of change of the spectrum we calculate with
                approximation method what the spectrum will be like next moment. The type of approximation is selected
                automatically, depending on the current pattern of changes of the spectrum components. This is
                necessary to be able to get a true forecast of the spectrum behaviour in future. This is mainly used in
                very expensive devices (so far only experimental models). In simple COSMODIC systems linear
                approximation is used. In personal and semi-professional models approximation is not used.
            </p>
            <p class="content-p">
                With phase cut-off method the device can shift the central component of the spectrum within the
                frequency scale, increasing or decreasing the frequency — spectral saturation of the acting impulse is
                being changed. At first, the spectrum is changed in one direction, e.g. increase, and the speed
                of body reaction upon the action is being traced. If the speed of the body reaction is positive, we
                continue changing the spectrum in this direction (continue increasing it), until we reach approximately
                zero speed of reaction. Then, we change direction of the spectrum change backwards (decreasing). And we
                continue changing it until the speed of reaction keeps being positive. When the speed of reaction
                reaches zero, we again change direction of the spectrum change backwards.
            </p>
