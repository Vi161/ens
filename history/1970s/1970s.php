                which was then known as zhen-jui method.
            </p>
            <p class="content-p">
                Reflexotherapy (as you can see from the term) is a part of the treatment concept based on the principle
                of reflex — the most universal mechanism of nervous activity.
            </p>
            <p class="content-p">
                One of the main reasons why this method of treatment became so poplar was that it allowed treating
                diseases without medications or significantly reducing the use of medications.
            </p>
            <p class="content-p">
                Another important thing here is that with professional treatment there are no side-effects, while most
                medications cause them.
            </p>
            <p class="content-p">
                Such approach is only successful when applied by professional acupuncturists, but even in this case it
                does not guarantee 100% result. Self-treatment is, of course, out of the question.
            </p>
            <p class="content-p">
                Using reflex mechanisms for self-treatment is a very good idea. And it can be used for treatment of
                nearly all diseases. As any irritation is transmitted in our body in the form of nerve (electrical)
                impulses, it should be possible to make a universal “electrical” pill, why not? That is what Alexander
                Karasev was thinking about in 1972.
            </p>
            <h2 class="content-h2">Magneto <span class="year">1973</span></h2>
            <p class="content-p">
                The first device consisted of an engine (taken out of a children’s toy) with contact breaker. The engine
                broke the contacts, which were connected to the transformer with battery (like magneto in a tractor).
                The transformer was connected to the electrodes. Impulses, which were generated by contact break
                (self-induction) were close to the nerve impulses. When the electrodes were placed on the skin in the
                area of pain sensations you could achieve pain-relief effect. This device was the ancestor of all TENS
                devices (devices for pain-relief).
            </p>
            <p class="content-p">
                With time, the toy engine was replaced with generator, the contacts were replaced with electronic
                switch, and the transformer was left as it was. This is how a TENS was born. And all TENS devices are
                still made on the same principle.
            </p>
            <h2 id="ens01" class="content-h2">ENS-01 <span class="year">1976</span></h2>
            <div class="hyst-block-1970s__img1"></div>
            <p class="content-p">
                The first model for practical use.<br>
                Casing was made of ABS plastic sheet, glued with dichloroethane and decorated with leatherette.<br>
                Two slide controls — “energy” and “frequency”.<br>
                Electrode was round-shaped, coaxial. Detachable!!!<br>
                Indication: one LED displaying action energy level.<br>
                Power supply: built-in accumulators.
            </p>
            <h2 id="ens02" class="content-h2">ENS-02 <span class="year">1979</span></h2>
            <div class="hyst-block-1970s__img2"></div>
            <p class="content-p">
